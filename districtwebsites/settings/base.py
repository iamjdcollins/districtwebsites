def load_secrets(file=os.path.join(BASE_DIR, '.secrets.json')):
    try:
        with open(file) as f:
            secrets = json.loads(f.read())
            return secrets
    except FileNotFoundError:
        raise ImproperlyConfigured(
            'Secrets file not found. Please create the secrets file or correct'
            ' the configuration.'
        )


