# Get a secret
def get_secret(key, secrets=secrets or load_secrets()):
    try:
        val = secrets[key]
        if val == 'True':
            val = True
        elif val == 'False':
            val = False
        return val
    except KeyError:
        error_msg = (
            "ImproperlyConfigured: Set {0} environment variable"
        ).format(key)
        raise ImproperlyConfigured(error_msg)


